package exam01;

public class Escape01 {
	public static void main(String[] args) {
		
//	제어문자	
		
//	\n : 줄바꿈
		System.out.println("안녕\n하세요");
		
//		\t : 일정한 간격을 띄움 
		System.out.println("반갑\t습니다");
		
//		\" \' : 문자열 안에 큰,작은 따옴표를 표현
		System.out.println("오늘은 \"3일차\" \'수업입니다\'");
		System.out.println("오늘은 '3일차'수업입니다");

//		\\ : 역슬래쉬
		System.out.println("제어문자는 \\로 시작한다");
		
		
	}
}
